    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
    if(checkFields('u')){
        int id = Integer.parseInt(input_id.getText());
        String name = input_name.getText();
        String unit = input_unit.getText();
        int qty = jSpinField_qty.getValue();
        int protein = jSpinField_protein.getValue();
        int fat = jSpinField_fat.getValue();
        int calori = jSpinField_calori.getValue();
        Food f;
        if(!fd.getById(id).getPicture().equals(imgPath)){
            String path = iu.uploadImage(imgPath, true);
            iu.deleteImage(fd.getById(id).getPicture());
            f = new Food(id,name, unit, qty, protein, fat, calori,path);
        }else
            f = new Food(name, unit, qty, protein, fat, calori, fd.getById(id).getPicture());
        
        if(fd.update(f)){
            JOptionPane.showMessageDialog(null, "Update Successful");
            dtm = FoodUtil.fillFoodTable(food_table, null);
        }
    }else{
    JOptionPane.showMessageDialog(null, "You must fill all inputs");
    }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if(checkFields('d')){
          int id = Integer.parseInt(input_id.getText());
          String oldFile = fd.getById(id).getPicture();
          if(fd.deleteFromId(id)){
            JOptionPane.showMessageDialog(null, "Delete Successful");
            dtm = FoodUtil.fillFoodTable(food_table, null);
            iu.deleteImage(oldFile);
            setEmptyFields();
          }
        }else{
        JOptionPane.showMessageDialog(null, "You must fill all inputs");
    }
    }//GEN-LAST:event_btn_deleteActionPerformed

