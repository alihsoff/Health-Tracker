    public void selectItem(int rowIndex){
        Food f = fd.getById((int)dtm.getValueAt(rowIndex, 0));
        
        input_id.setText(String.valueOf(f.getId()));
        input_name.setText(String.valueOf(f.getName()));
        input_unit.setText(String.valueOf(f.getUnit()));
        jSpinField_qty.setValue(f.getQty());
        jSpinField_protein.setValue(f.getProtein());
        jSpinField_fat.setValue(f.getFat());
        jSpinField_calori.setValue(f.getCalori());
        iu.setImage(f.getPicture(), lbl_foodImage);
    }
    
    public void setEmptyFields(){
        input_id.setText("");
        input_name.setText("");
        input_unit.setText("");
        jSpinField_qty.setValue(0);
        jSpinField_protein.setValue(0);
        jSpinField_fat.setValue(0);
        jSpinField_calori.setValue(0);
        iu.setImage(null, lbl_foodImage);
    }
    

