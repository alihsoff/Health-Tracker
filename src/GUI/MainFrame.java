    @Override
    public boolean checkFields() { // For Add btn
    if(input_name.getText().isEmpty() || input_unit.getText().isEmpty() ||
            jSpinField_qty.getValue()==0 || jSpinField_protein.getValue()==0 ||
            jSpinField_fat.getValue()==0 || jSpinField_calori.getValue()==0)
        return false;
    return true;
    }
    
    public boolean checkFields(char o) { // For Update and Delete
    if(o=='d'&&!input_id.getText().isEmpty())
            return true;
       
    else if(input_id.getText().isEmpty()||
            input_name.getText().isEmpty() || input_unit.getText().isEmpty() ||
            jSpinField_qty.getValue()==0 || jSpinField_protein.getValue()==0 ||
            jSpinField_fat.getValue()==0 || jSpinField_calori.getValue()==0)
        return false;
        
    return true;
    }
    
